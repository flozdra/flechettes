const board = ref<SVGSVGElement>();
const tooltip = ref({ dartThrow: null as DartThrow | null, x: 0, y: 0 });

/**
 * Retrieve the coordinates of the SVG dartboard from the mouse event
 */
function getThrowCoordinates(evt: MouseEvent): ThrowCoordinates {
  const point = board.value?.createSVGPoint();
  const matrix = board.value?.getScreenCTM();
  if (!point || !matrix) return { x: 0, y: 0 };
  point.x = evt.clientX;
  point.y = evt.clientY;
  // The cursor point, translated into svg coordinates
  const matrixPoint = point.matrixTransform(matrix.inverse());
  return {
    x: Number((matrixPoint.x - 7).toFixed(0)),
    y: Number((matrixPoint.y - 7).toFixed(0)),
  };
}

function handleClick(evt: MouseEvent, dartThrow: DartThrow) {
  evt.stopPropagation();
  emit("hit", dartThrow, getThrowCoordinates(evt));
  soundEffects.hitDart.play();
}

function handleMove(evt: MouseEvent, dartThrow: DartThrow) {
  evt.stopPropagation();
  const { x, y } = getThrowCoordinates(evt);
  tooltip.value = { dartThrow, x, y };
}

