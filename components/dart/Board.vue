/**
 * Redraw the hit markers whenever the hits change.
 * We need to wait for the component to be mounted to access the SVG element.
 */
onMounted(() => {
  watch(
    () => props.hits,
    (hits) => {
      if (!board.value) return;

      // Remove all previous markers
      const markers = board.value.querySelectorAll(".hit-marker");
      markers.forEach((marker) => marker.remove());

      // Add new markers
      hits.forEach(drawHitMarker);
    },
    { immediate: true, deep: true },
  );
});

/**
 * Define the attributes for each segment of the dartboard
 */
function getAttributes(dartThrow: DartThrow) {
  const showHighlights =
    settings.value.highlightNumbers && !!props.highlights?.length;
  const isHighlighted = props.highlights?.includes(dartThrow.id);

  return {
    fill: dartThrow.color,
    stroke: DartColors.Beige,
    "stroke-width": showHighlights ? 0 : 1,
    class: [
      dartThrow.color === DartColors.Green && "green-section",
      dartThrow.color === DartColors.Red && "red-section",
      showHighlights && !isHighlighted && " brightness-40",
    ],
    onmouseup: (evt: MouseEvent) => handleClick(evt, dartThrow),
    onmousemove: (evt: MouseEvent) => handleMove(evt, dartThrow),
  };
}

/**
 * Handle the mouse move event outside the valid segments to hide the tooltip
 */
function handleMoveOut(evt: MouseEvent) {
  evt.stopPropagation();
  tooltip.value = { dartThrow: null, x: 0, y: 0 };
}

